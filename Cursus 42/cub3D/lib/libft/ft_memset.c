#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	char	*s1;
	size_t	i;

	s1 = (char *)s;
	i = 0;
	while (i < len)
	{
		s1[i] = c;
		i++;
	}
	return (s);
}
